package server;

public class UserHistory {

    long day;
    double energyUsed;

    public long getDay() {
        return day;
    }

    public void setDay(long day) {
        this.day = day;
    }

    public double getEnergyUsed() {
        return energyUsed;
    }

    public void setEnergyUsed(double energyUsed) {
        this.energyUsed = energyUsed;
    }
}
